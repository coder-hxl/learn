// 1.类型注解
// var/let/const 标识符: 数据类型 = 赋值
var name: string = 'fuhuan'
let age: number = 17
const height: number = 1.80

// 2.string和String的区别
// string: TypeScript中的字符串类型
// String: JavaScript的字符串包装类的类型
const message: string = 'Hello'

// 3.类型推导
// 默认情况下进行赋值时，会将值的类型，作为前面标识符的类型，这个过程称之为类型推导/推断
let foo = 'foo'
// foo = 666


